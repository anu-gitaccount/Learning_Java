    public static  void main(String[] args) {

        // Wrapper class = alows a way to use primitive data type as refrence datetypes
        //                 reference datatypes has useful methods

        // Primitive                // Wrapper
        // int                      // Integer
        // boolean                  // Boolean
        // double                   // Double


        // autoboxing = automatic conversion that javac makes primitive to refrence
        // unboxing = reverse automatic conversion

        // Refrence data type var
        Integer num = 10;
        Character ch = 'a';
        Boolean valid = false;
        Double dec = 10.000;



    }
